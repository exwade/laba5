def students():
    # Списки фамилий студентов двух групп
    group1 = ['Иванов', 'Петров', 'Сидоров', 'Козлов', 'Николаев', 'Кузнецов', 'Смирнов', 'Морозов', 'Васильев', 'Зайцев']
    group2 = ['Соколов', 'Лебедев', 'Ковалев', 'Попов', 'Волков', 'Медведев', 'Алексеев', 'Григорьев', 'Борисов', 'Максимов']

    # Объединяем по 5 студентов из каждой группы в спортивную команду (кортеж)
    sports_team = tuple(group1[:5] + group2[:5])

    # Выводим исходные списки групп и новый кортеж спортивной команды
    print("Фамилии студентов группы 1:", group1)
    print("Фамилии студентов группы 2:", group2)
    print("Спортивная команда:", sports_team)

    # Выводим длину кортежа спортивной команды
    print("Длина спортивной команды:", len(sports_team))

    # Сортируем кортеж по алфавиту
    sorted_team = tuple(sorted(sports_team))
    print("Отсортированная спортивная команда по алфавиту:", sorted_team)

    # Проверяем наличие фамилии "Иванов" в команде и считаем количество вхождений
    surname_to_check = 'Иванов'
    if surname_to_check in sports_team:
        count_ivanov = sports_team.count(surname_to_check)
        print(f"Студент {surname_to_check} входит в команду.")
        print(f"Фамилия {surname_to_check} встречается {count_ivanov} раз(а) в команде.")
    else:
        print(f"Студент {surname_to_check} не входит в команду.")

students()
